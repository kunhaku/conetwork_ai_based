
import React from 'react';
import { ResearchReport, UnifiedGraph, GraphNode } from '../types';

interface ReportPanelProps {
  graphData: UnifiedGraph;
  onNodeClick: (node: GraphNode) => void;
}

const ReportPanel: React.FC<ReportPanelProps> = ({ graphData, onNodeClick }) => {
  const { report } = graphData;

  if (!report) {
    return (
      <div className="flex-1 flex items-center justify-center text-gray-400">
        <p>No report generated.</p>
      </div>
    );
  }

  const findNode = (id: string) => graphData.nodes.find(n => n.id === id);

  return (
    <div className="flex-1 bg-white h-full overflow-y-auto custom-scrollbar p-8 max-w-4xl mx-auto">
      <div className="mb-8 border-b border-gray-200 pb-6">
        <h1 className="text-3xl font-serif text-gray-900 font-bold mb-2">Strategic Research Report</h1>
        <p className="text-gray-500 text-sm">Generated by Agent R • NexusGraph AI</p>
      </div>

      {/* Overview */}
      <section className="mb-10">
        <h2 className="text-lg font-bold text-gray-800 mb-3 border-l-4 border-blue-500 pl-3">Executive Overview</h2>
        <p className="text-gray-700 leading-relaxed whitespace-pre-line">{report.themeOverview}</p>
      </section>

      <div className="grid grid-cols-1 md:grid-cols-2 gap-8 mb-10">
        {/* Key Players */}
        <section>
          <h2 className="text-lg font-bold text-gray-800 mb-4 border-l-4 border-emerald-500 pl-3">Critical Players</h2>
          <div className="space-y-4">
            {report.keyPlayers.map((item, idx) => {
              const node = findNode(item.nodeId);
              return (
                <div key={idx} className="bg-emerald-50/50 p-4 rounded-lg border border-emerald-100 hover:shadow-md transition-shadow cursor-pointer" onClick={() => node && onNodeClick(node)}>
                  <div className="font-bold text-emerald-900 mb-1">{node?.name || item.nodeId}</div>
                  <p className="text-sm text-emerald-800">{item.rationale}</p>
                </div>
              );
            })}
          </div>
        </section>

        {/* Second Tier / Hidden Gems */}
        <section>
          <h2 className="text-lg font-bold text-gray-800 mb-4 border-l-4 border-violet-500 pl-3">Hidden Opportunities</h2>
          <div className="space-y-4">
            {report.secondTierBeneficiaries.map((item, idx) => {
              const node = findNode(item.nodeId);
              return (
                <div key={idx} className="bg-violet-50/50 p-4 rounded-lg border border-violet-100 hover:shadow-md transition-shadow cursor-pointer" onClick={() => node && onNodeClick(node)}>
                  <div className="font-bold text-violet-900 mb-1">{node?.name || item.nodeId}</div>
                  <p className="text-sm text-violet-800">{item.rationale}</p>
                </div>
              );
            })}
          </div>
        </section>
      </div>

      {/* Risks */}
      <section className="mb-10">
        <h2 className="text-lg font-bold text-gray-800 mb-4 border-l-4 border-red-500 pl-3">Risk Assessment</h2>
        <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
            {report.riskNodes.map((item, idx) => {
                const node = findNode(item.nodeId);
                return (
                    <div key={idx} className="flex items-start gap-3 p-3 rounded-lg bg-red-50 border border-red-100">
                        <span className="text-red-500 font-bold mt-1">⚠️</span>
                        <div>
                             <div className="font-bold text-red-900 text-sm mb-0.5 cursor-pointer hover:underline" onClick={() => node && onNodeClick(node)}>
                                {node?.name || item.nodeId}
                             </div>
                             <p className="text-xs text-red-800">{item.riskFactor}</p>
                        </div>
                    </div>
                )
            })}
        </div>
      </section>

      {/* Next Steps */}
      <section className="mb-12 bg-gray-50 p-6 rounded-xl border border-gray-200">
        <h2 className="text-lg font-bold text-gray-800 mb-4">Recommended Actions</h2>
        <ul className="space-y-2">
            {report.suggestedNextSteps.map((step, idx) => (
                <li key={idx} className="flex items-start gap-2 text-sm text-gray-700">
                    <span className="text-blue-500 font-bold">•</span>
                    {step}
                </li>
            ))}
        </ul>
      </section>

      {/* Disclaimer */}
      <footer className="text-xs text-gray-400 text-center border-t border-gray-100 pt-6">
        <p className="font-bold mb-1">DISCLAIMER</p>
        <p>{report.disclaimer}</p>
      </footer>
    </div>
  );
};

export default ReportPanel;
